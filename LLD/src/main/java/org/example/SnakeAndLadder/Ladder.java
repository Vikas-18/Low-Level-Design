package org.example.SnakeAndLadder;

public class Ladder {
    int startRow;
    int startCol;
    int endRow;
    int endCol;

    Ladder(int startRow,int startCol,int endRow,int endCol)
    {
        this.startCol = startCol;
        this.startRow = startRow;
        this.endRow = endRow;
        this.endCol = endCol;
    }

}
