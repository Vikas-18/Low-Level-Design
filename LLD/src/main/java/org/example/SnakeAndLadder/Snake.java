package org.example.SnakeAndLadder;

public class Snake {
    int startRow;
    int startCol;
    int endRow;
    int endCol;

    Snake(int startRow, int startCol, int endRow, int endCol)
    {
        this.startCol = startCol;
        this.startRow = startRow;
        this.endRow = endRow;
        this.endCol = endCol;
    }


}
